
// Single line comment
/*
 * Multi-line comment
 */

#include <iostream>

int main() {
    std::cout << "Line 1" << std::endl;
    std::cout << "Line 2" << std::endl;

    std::cout << "Line 3\n";
    std::cout << "Line 4\n";

    std::cout << "First " << "Second " << "Third" << std::endl;
    std::cout << "The number is: " << 42 << std::endl;

    std::cout << "Tab:\tThis is tabbed" << std::endl;
    std::cout << "Quote: \"Hello\"" << std::endl;
    std::cout << "Backslash: \\" << std::endl;

    return 0;
}
